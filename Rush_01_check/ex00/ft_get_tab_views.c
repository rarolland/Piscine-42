int	**ft_get_tab_views(char *argv, int **tab_views)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 0;
	k = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			tab_views[i][j] = argv[k];
			j++;
			k++;
		}
		j = 0;
		i++;
	}
	return (tab_views);
}
